using System;

class Variaveis {
     static void Main (string[] args) {
        // Aqui criei uma variável de número INTEIRO, com o valor 15
        int numero = 15;
        Console.WriteLine(numero + 19);
        Console.WriteLine(numero + 38);
        Console.WriteLine(numero + 40);
        Console.WriteLine(numero + 79);
        Console.WriteLine(numero + 108);
        Console.WriteLine(numero + 276);

        int idade = 21; // Números inteiros
        double altura = 1.70; // Números com ponto flutuante
        char inicial = 'A'; // Um único caractere
        string nome = "Artur Fellipe Pellenz Ferreira"; //Texto
        bool aprovado = true; // Verdadeiro ou Falso

        Console.WriteLine($"Meu nome é {nome}. Tenho {idade} anos. Minha altura é {altura}. Meu nome começa com a letra {inicial}. Estou aprovado na escola? {aprovado}.");

     }
}

